TSTKB:		CALL PS2KEYAVAIL
		JR NZ,TSTKB				
		CALL READKBCHAR	;GET THE KEY FROM USB KEYBOARD (HAS A TIMEOUT)
		OR A
		JR Z,TSTKB				
		PUSH AF
		CALL RS_TX
		LD A,32
		CALL RS_TX
		POP AF				
		CALL SOUTAS
		LD A,(LASTKEY)
		CALL SOUTAS
		LD A,(KEYCTR)
		CALL SOUTAS
		LD A,32
		CALL RS_TX
		LD A,13
		CALL RS_TX
		JR TSTKB


